          <h3>Introduction</h3>
          <hr>
          <p>In Kenya, youth unemployment is a serious development issue. It is estimated that 64% of unemployed persons in Kenya are youth. Interestingly only 1.5% of the unemployed youth have formal education beyond secondary school level and the remaining over 92% have no vocational or professional skills training and the majority are found in the rural Kenya. Due to inadequate opportunities in rural areas the tendency is that they migrate to urban centres to look for such opportunities.
 </p>
 <p>
Kenyan economy heavily depends on Agriculture (30% of GDP), which is basically a rural-oriented sector. Surprisingly, Kenyan agriculture is still labour-intensive thus the out-migration of young and productive labour force from rural to urban centres has a direct negative impact on agricultural production hence job creation in other sectors which are directly or indirectly linked to the sector will be reduced. 
</p>
<p>
It’s in this regard, Fair Trade Organization of Kenya (FTOK) and Agri Hub Kenya developed the idea to organise a youth challenge that will highlight innovative and creative ideas that youth would take up in agriculture and the value chains. The overall objective of the online youth challenge is to spur youth-led ideas in agriculture and agri-business. The youth challenge seeks among other things to generate an interest from the youth to participate more in agriculture while devising innovative ways to upscale the number of youth that are active in agriculture.
</p>
<p> The youth challenge will also move towards promoting agribusiness where the youth will look at farming as a business and thus a “cool” thing to be engaged in. In line with Agri Pro Focus core mission, we seek to promote farmer entrepreneurship by promoting farming as a business rather than the conventional way of looking at agriculture as a retirement activity that the older generations engage in after employment. 
</p>

