    <div class="span12 well" style="margin-left: 0px;width: 110%;">
    <p>FTOK has been working with several cooperatives in East Africa to uplift the livelihoods of smallholder producers. Through improved productivity and streamlined marketing systems, smallholder producers are able increase incomes from on and off farm enterprises hence improved living standards. </p>
<h4>Food security</h4>
<p>
Population growth has been growing geometrically with food production assuming arithmetic model in the recent past hence posing a lot of challenge in food security. Due to this, FTOK has taken the initiative to train farmers on diversification to increase on food security. FTOK lays more emphasis on every house hold to have a chicken, goat and a cow so that they can have a balanced diet. Food security exists when all people, at all times, have physical, social and economic access to sufficient, safe and nutritious food to meet their dietary needs and food preferences for an active and healthy life</p>
<p>There is strong, direct relationships between agricultural productivity, hunger, poverty, and sustainability. Three-quarters of the world's poor live in rural areas make their living from agriculture. Hunger and child malnutrition are greater in these areas than in urban areas. Moreover, the higher the proportion of the rural population that obtains its income solely from subsistence farming (without the benefit of pro-poor technologies and access to markets), the higher the incidence of malnutrition. Therefore, improvements in agricultural productivity aimed at small-scale farmers will benefit the rural poor first. 
</p>

<h4>Housing</h4>
<p>
Due to the recent increase in population growth in Kenya, the housing sector has been unable to provide sufficient affordable housing to its residents. The shortage of both land and housing is a result of a complicated land tenure history, inefficient regulatory frameworks, and under-resourced projects to improve housing. Those most affected by the lack of housing are primarily from lower income groups and farmers. FTOK in its effort to increase coffee production and quality of coffee has made coffee farmers to start building better and permanent houses hence improved living standards
</p>
<h4>Health</h4>
<p>
Health is a very important aspect of life. Improved food security leads to improved health. Due to continuous capacity building by FTOK farmers have been able to seek for health care from both government hospitals and private dispensaries. Tropical diseases, especially malaria and tuberculosis, have long been a public health problem in developing countries. In recent years, infection with the human immunodeficiency virus (HIV/AIDS) also has become a severe problem. FTOK in all its programs has put emphasis in educating farmers on HIV/AIDS. FTOK has created awareness to farmers about the diseases and how to deal with them. 
</p>

