    <span class="span12 label label-success" style="padding: 8px;text-align: justify;width: 110%;margin-bottom: 10px;"><h4>Product certification</h4></span>
    <div class="span12 well" style="margin-left: 0px;width: 110%;">

<p>
<b>Product certification</b> is the process of certifying that a certain product has passed performance tests and quality assurance tests, and meets qualification criteria stipulated in contracts, regulations, or specifications. Product certification indicates a product's suitability to be distributed to the masses safely, guaranteeing that whatever's in the labels are real. It also means that the merchandises is distributed and sold fairly between the producers, consumers and middlemen. Standards and requirements depend on what kind of product is dealt with and the factors surrounding its use and production
</p>
<p>
Product certification is important since it assures that the product is suited for distribution and public consumption; assures producers are given a fair share of the sale of their products; signifies desire of producer organizations to improve their profit fairly and honestly without taking advantage of smallholders producers and/or consumers; demonstrates consumers' awareness not only for their welfare, but also the perpetuation of quality goods and services through mandatory certification; allows consumers to purchase quality goods for the right price; compels producers to provide the best quality goods at all times as it raises the overall bar for goods and services in the market; and  promotes good production practices while rewarding the efforts of consumers</p>
<p>
FTOK supports producer organizations to acquire and maintain certification standards of their products. This is done through capacity building of producers and producer organizations on the benefits of certified products; sensitizing producer organizations on requirements of various certifications standards and building their capacity to attain them; linking producer organizations to certification bodies to acquire certificate of conformity and approval to apply product certification mark; conducting periodic monitoring and evaluation to ensure producer organizations comply to the certification standards;  linking producer organizations to direct market of certified products to ensure fair returns of their products; and  instituting Internal Control Systems to the producer Organizations to ensure sustainable compliance to the product certification standards

</p>

