<p>The FTOK obligation is to strengthen producer organizations to serve their members efficiently and effectively. Through such organizations, smallholder producers are cushioned from productions and marketing constraints whose spiral effect results in price volatility making them undergo huge losses from their agricultural enterprises.</p>
<p>Strong producer organizations are able to aggregate produce, add value to the products and collectively market on behalf of its farmers. By this approach, farmers are able to get higher returns from their produce since they enjoy economies of scale. FTOK approach has always been bringing together producers of various commodities to form strong organizations or supporting established organizations to make them strong to thrive in competitive business environment.</p>
<p>Cooperatives have demonstrated as best device for assisting marginalized smallholder producers to overcome many challenges arising from globalization and market liberalization. FTOK has been on the forefront to strengthen cooperatives among other producer organizations.
</p>
<p>
 How does FTOK promote Organizational Development?
FTOK employs holistic approach in promoting organizational development using the following strategies;
<ul>
<li>Capacity  on Leadership and Corporate Governance of leaders and  to manage Producer organization effectively</li>
<li>Development of Policy documents to embrace good business practices (Strategic plan, business plan, HR policy, Quality management plan, financial policy, marketing plan)</li>
<li>Capacity building on  Financial Management and linking producer groups to FIs to access finance</li>
<li>Capacity building on Record keeping and Data Management through ICT </li>
<li>Capacity building on  Marketing and linking producer groups to the direct markets, nationally and internationally</li>
</ul>
</p>

