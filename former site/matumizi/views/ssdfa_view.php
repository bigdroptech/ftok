    <span class="span12 label label-success" style="padding: 8px;text-align: justify;width: 100%;margin-bottom: 10px;"><h4>FTOK Partners with Small Scale Dairy Farmer Association (SSDFA)</h4></span>
    <br>
    <p>
In an effort to deliver the SSDFA's mandate at the national level more effectively as well as establishment of strong linkages with dairy industry players, a successful partnership between SSDFA and Fair Trade Organization of Kenya (FTOK) was established.
</p>
<p>
The Fair Trade organization of Kenya (FTOK) is a Non Governmental capacity building organization with the main focus of creating equitable and sustainable channels in production and trade. FTOK seeks to increase the presence certified products in the market locally and internationally. It brings together producer organizations, processors, marketers, consumers and other stakeholders in an effort to realize favorable trading standards and achieve strong and lasting trade relationships.
</p>
<p>
The organization is engaged in coordinating grower, trader, importer and other stakeholder's activities ensuring that otherwise complex systems are streamlined through fair trade practices and advocacy. Its activities are designed to empower the producers on business development and accountability skills all geared towards reducing poverty levels within communities and stimulating rural economies through growth linkages between on farm and off farm enterprises.
</p>
<p>
While adopting a holistic approach to creating equitable and sustainable channels in production and trade by understanding and acting on the overall context, the thrust of FTOK's approach and strategy is designing and implementing programmes that are aimed at promoting the achievement of responsibility, accountability and transparency among the producers, buyers and other middle players involved in marketing systems. Empowerment of small scale producers to actively participate not only in production but also in trade, building local institutional/organisational capacity, advocating and influencing policy changes are at the heart of FTOK's work.
</p>
<p>
FTOK's major approach to work is engaging in partnership with producer groups to strengthen them and to build a greater force against poverty and responding to the global outcry for more food secure households and developing secure and sustainable live skills especially among women and youth for economic stability.
</p>
<p>
The main objective behind the SSDFA/FTOK partnership is to build the capacity of SSDFA on their roles and responsibilities in serving their members; assist in crafting of policy documents for the Association; Technical Assistance to members on good dairy husbandry; access to Finance; Access to Markets; Value addition; Environment and Climatic adaptation & Mitigation; and Integration of cross cutting issues (gender, youth, HIV/AIDS) in Dairy farming.
</p>
<p>
Specifically, FTOK plays the role of Service Provider and aims at building the capacity of SSDFA and her members to build equitable and sustainable production and trade mechanisms, thus enhancing contribution of SSDFA to socio-economic development and poverty alleviation. 
</p>
