    <span class="span12 label label-success" style="padding: 8px;text-align: justify;width: 100%;margin-bottom: 10px;"><h4>Launch of Coffee Agronomy Farmers Manual</h4></span>
    <br>
    <p>
The FTOK prowess in supporting smallholder coffee producers has culminated in Production of Coffee Agronomy Farmers Manual. The manual is the most recent and readable manual in the coffee sector that has a complete cycle form seedling preparation to primary processing. The manual was developed as a result of FTOK capacity building to the producer groups and hence farmers demanded for a reference material</p>
<p>
The book will enable coffee farmers to enhance quality and quantity, improve basic skills on how to produce coffee, provide guidance on environmental improvement and conservation and give information on how to adapt and mitigate the effect of climate change. The book contains simplified information to enable farmers to read, apply and achieve good results. 
The manual is currently being translated into local dialects.
</p>
<p>This will enhance understanding and implementation of various practices explained in the manual in an effort reach out to more coffee farmers hence improve coffee productions and enhance quality.
The successful development of this manual was as a result of combined efforts with our partners; Deloitte and Progreso. Coffee Research Foundation (CRF) was also involved in the editing and compiling of the manual.
</p>
