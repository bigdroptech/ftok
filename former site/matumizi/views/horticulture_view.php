    <span class="span12 label label-success" style="padding: 8px;text-align: justify;width: 100%;margin-bottom: 10px;"><h4>Horticulture Farming</h4></span>
    <br>
    <p>
    Horticultural crops include flowers, fruits, vegetables and potatoes. The horticulture sub-sector provides food security. Currently the horticulture industry is the fastest growing agricultural sub- sector and provides foreign exchange earnings. Fruits, vegetable and cut flower production are the main aspects of horticultural production in Kenya. FTOK does capacity building in horticulture farming to improve food security. 
Coffee growing regions are densely populated. Food insecurity in these areas is immense. Coffee is paid twice a year and therefore small producers need to diversify to other food crops and income generating products. 
</p>
    <p>
Since the small producers have small piece of land it is important to intercrop coffee with other food crops which can grow together. These are crops like bananas, macadamia nuts, avocados, mangoes and pawpaws.  
</p>
<br>
<h4>Bananas</h4>
<hr>
<p>
When banana and coffee are grown together farmers tend to earn more. Growing coffee and banana forms the economic base for most of the small-scale farmers.
Banana is an important staple food in Kenya and produced all year round; farmers sell surplus yield on a daily basis.  Bananas has high value energy giving food and hence good for small children. It also acts as a fruit when it ripens and provides vitamins. Coffee, on the other hand, is a pure cash crop, cultivated on small pieces of land by small producers. It is a major foreign exchange earner for Kenya.
</p>
<p>
  The other advantages of growing coffee under bananas are it provides shade, mulch, nutrients, and moisture. For instance, farmers use coffee husks to replenish nutrients in banana and coffee fields. It also reduces soil erosion in highlands.
The crops complement one another in terms of socioeconomic benefits to growers and farm families. Bananas provide permanent food and income security, doubling as a primary food and cash crop, and providing a modest but continuous cash flow throughout the year. Coffee gives a cash boom twice a year, helping farmers acquire funds for more expensive items such as infrastructure, School fees, health care, farm inputs, and transport equipment.
</p>
<br>
<h4>Avocado</h4>
<hr>
<p>
Avocado just like coffee does well in temperate climate.  This plant is termed a dark skinned avocado as it turns dark purple when ready to eat. The majority of Kenyan avocados are for exports. The temperate climate is perfectly suited to high quality avocado production and it is known for its good taste and eating quality. It has vegetative fat with no cholesterol. It has nutrition value and it's a food security. The pruning's can be used as charcoal or firewood.
</p>
<p>
Avocado can be interplanted with coffee with a spacing of about 15m apart. It provides shade to the coffee bushes and hence moderates the temperatures. Coffee under shade does not have the bi-annual production since the weather is moderated. The production is regulated every year.   
</p>
<br>
<h4>Macadamia</h4>
<hr>
<p>Macadamia nut provides fat, protein and has a nice taste. They are used for export and farmers get income from them. They provide firewood after pruning.  The hard part of the nut is used as fuel.</p>
<br>
<h4>Papaws</h4>
<hr>
<p>Papaws fruits trees grow in tropical and subtropical climates. Pawpaws fruits are delicious and grow throughout the year. These fruits are eaten alone or in salad without the skin. The papaws fruits are low in calories and high in potassium, vitamin A and c. Pawpaws enzymes promote digestion easing constipation and it is efficient in controlling colon infections. The plants produce throughout the year. Pawpaws are sold in both local markets and for exports. They can be intercropped with coffee.
</p>
