    <span class="span12 label label-success" style="padding: 8px;text-align: left;width: 100%;margin-bottom: 10px;"><h4>Dairy Farming</h4></span>
    <br>
    <p>
    Dairy farming is currently attracting a lot interest to the smallholder producers due to the numerous benefits that can be tapped from the enterprise. Besides providing solution to food security, producers are able to earn incomes from produces and by-products, support agricultural production through provision of farmyard manure to supplement inorganic fertilizers whose cost has escalated beyond reach of majority of smallholder producers and conserve energy through biogas technology</p>
<p>
The overriding issues afflicting smallholder dairy producers in Africa need to be addressed for the farmers to tap the benefits that accrue from this highly potential enterprise. Through hand on experience in the Dairy industry, FTOK has been able to identify major challenges facing smallholder dairy producers which revolves around, Herd management; Breeds and breeding; Animal health; Feeds and feeding; Milk production; food safety and hygiene; fragmented value chain; price differentials; Government policies and legislations on Dairy Industry; Institutional capacity building; Technology Transfer systems; Value addition and Marketing</p>
<p>
The FTOK through a documentary, 'A Focus on Dairy farming' briefly highlights some issues that need to be addressed in Dairy farming and Interventions required
