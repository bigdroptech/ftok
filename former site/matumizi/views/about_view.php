    <span class="span12 label label-success" style="padding: 8px;text-align: left;width: 110%;margin-bottom: 10px;"><h4>Fair Trade Organisation Of Kenya</h4></span>
    <div class="span12 well" style="margin-left: 0px;width: 110%;text-align: justify;">
    <p>Fair Trade Organization of Kenya (FTOK) is a nonprofit international network organization whose main aim is to create equitable and sustainable channels in production and trade.</p>

<p>The Organization works in close collaboration and partnership with national and international networking organizations to make fair trade a reality and a viable development tool</p>

<p>FTOK works closely with smallholder producers in Agricultural sector which is a key contributor of Gross Domestic product (GDP) in many developing countries</p>

<p>The Organization brings together smallholder producers in marginalized farming communities and empowers them to participate more actively not only in the production level but also in marketing of their produce</p>

<p>The FTOK believes in viable strong producer organizations and works towards ensuring smallholder producers form formidable organizations to enjoy economies of scale by aggregating their produce/products and marketing together. Through this approach, FTOK promotes direct market, both nationally and internationally, to ensure maximum returns to the smallholder producers</p>

<p>The FTOK leverages traders and producers expectations through free flow of information, both in electronic and print media. This empowers producers to take full ownership of their products. FTOK also empowers producers to participate in local and international arenas that bring more awareness on improving productivity and expanding markets and marketing opportunities.</p>

<p>The organization strengthen the Fair Trade movement by helping producers take greater ownership of their products as well as take active participation in export markets.</p>

<p>FTOK cornerstone is anchored on six basic pillars: Secure livelihoods for smallholder producers; Women and youth empowerment; Entrepreneurship development; Environmental and climate change adaptation and mitigation; Organizational development; and Promotion of Fair trade</p>

