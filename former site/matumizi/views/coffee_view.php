    <p>
    Coffee is one of the most traded commodities in the world. A substantial quantity of coffee traded in the international markets is from Africa and Kenya contributes a significant volume. The Kenyan coffee has unique appealing characteristics liked by many consumers. In Africa, coffee is grown by smallholder famers on small pieces of land.</p>
<p>
