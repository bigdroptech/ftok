    <span class="span12 label label-success" style="padding: 8px;text-align: justify;width: 100%;margin-bottom: 10px;"><h4>FTOK Manages the Progreso Platform</h4></span>
    <br>
    <p>
Progreso Network (PN) is an online community connecting coffee producer organizations and their business partners in their efforts towards more sustainable supply chain. The PN provides a platform for producer organizations and their partners for sharing knowledge and work on joint initiatives that contribute to a more sustainable supply chain.
</p>
<p>
The PN is moving towards South in order to be closest to the producer groups on a partnership with Fair Trade Organization of Kenya (FTOK). 
The FTOK, a Non Governmental organization, is a networking organization that seeks to create partnerships between producer organizations and their business partners for the purpose of learning and sharing knowledge. 
</p>
<p>
The FTOK will be managing the community for English speaking members. To deepen the interactions with members, FTOK will conduct recurrent activities, namely, Webinars, working groups, Challenges, blog posts and Newsletters. Contributions such as Interviews, Videos, Documentary, Articles and Events will also be uploaded for Knowledge sharing between the members.
</p>
